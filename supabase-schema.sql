-- ClasseHub MVP schema + seed data

create table public.classes (
  id bigint generated by default as identity primary key,
  slug text not null unique,
  name text not null,
  emoji text default '🏫',
  school_year text default '2025-2026',
  created_at timestamp with time zone default now()
);

create table public.families (
  id bigint generated by default as identity primary key,
  class_id bigint not null references public.classes(id) on delete cascade,
  student_name text not null,
  created_at timestamp with time zone default now()
);

create table public.events (
  id bigint generated by default as identity primary key,
  class_id bigint not null references public.classes(id) on delete cascade,
  title text not null,
  description text,
  details text,
  event_type text not null default 'classe',
  start_date date not null,
  start_time text,
  end_date date,
  location text,
  is_important boolean default false,
  created_at timestamp with time zone default now()
);

create table public.tasks (
  id bigint generated by default as identity primary key,
  class_id bigint not null references public.classes(id) on delete cascade,
  title text not null,
  description text,
  details text,
  due_date date,
  related_event_id bigint references public.events(id) on delete set null,
  is_important boolean default false,
  created_at timestamp with time zone default now()
);

create table public.checklist_items (
  id bigint generated by default as identity primary key,
  event_id bigint not null references public.events(id) on delete cascade,
  text text not null,
  sort_order integer default 0
);

create table public.polls (
  id bigint generated by default as identity primary key,
  class_id bigint not null references public.classes(id) on delete cascade,
  question text not null,
  description text,
  close_date date,
  is_active boolean default true,
  created_at timestamp with time zone default now()
);

create table public.poll_options (
  id bigint generated by default as identity primary key,
  poll_id bigint not null references public.polls(id) on delete cascade,
  text text not null
);

create table public.poll_votes (
  id bigint generated by default as identity primary key,
  poll_id bigint not null references public.polls(id) on delete cascade,
  option_id bigint not null references public.poll_options(id) on delete cascade,
  family_id bigint not null references public.families(id) on delete cascade,
  created_at timestamp with time zone default now(),
  unique (poll_id, family_id)
);

create table public.organizations (
  id bigint generated by default as identity primary key,
  class_id bigint not null references public.classes(id) on delete cascade,
  title text not null,
  description text,
  date date,
  external_url text,
  is_active boolean default true,
  created_at timestamp with time zone default now()
);

