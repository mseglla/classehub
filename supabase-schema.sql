create policy "Public read tasks" on public.tasks for select to anon using (true);
create policy "Public read checklist" on public.checklist_items for select to anon using (true);
create policy "Public read polls" on public.polls for select to anon using (true);
create policy "Public read poll options" on public.poll_options for select to anon using (true);
create policy "Public read poll votes" on public.poll_votes for select to anon using (true);
create policy "Public insert poll votes" on public.poll_votes for insert to anon with check (true);
